    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $data = $this->validate($request, [
            'firstname' => ['required', 'string', 'max:255'],
            'lastname' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:users,email,' . Auth::user()->id . ',id'],
            'address' => ['required', 'max:255', "string"],
            'phone' => ['required', 'string', 'regex:/(^(\+88|0088)?(01){1}[3456789]{1}(\d){8})$/i', 'unique:users,phone,' . Auth::user()->id . ',id'],
        ]);
        if (User::where($data)->exists()) {
            return back()->with(notification("info", "Looks like you haven't changed anything!"));
        }
        if (User::where('id', Auth::id())->update($data)) {
            return back()->with(notification("success", "Successfully Profile Updated"));
        } else {
            return back()->with(notification("error", "Something Wrong!"));
        }
    }

